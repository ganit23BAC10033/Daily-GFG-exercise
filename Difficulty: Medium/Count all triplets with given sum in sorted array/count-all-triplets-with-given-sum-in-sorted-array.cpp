class Solution {
  public:
    int countTriplets(vector<int> &arr, int target) {
        int count = 0;
        int n = arr.size();
        
        // Iterate through all possible triplets
        for (int i = 0; i < n - 2; i++) {
            for (int j = i + 1; j < n - 1; j++) {
                for (int k = j + 1; k < n; k++) {
                    if (arr[i] + arr[j] + arr[k] == target) {
                        count++;
                    }
                }
            }
        }
        
        return count;
    }
};


